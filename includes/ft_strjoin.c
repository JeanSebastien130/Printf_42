#include "libft.h"

char	*ft_strjoin(char const *s1, char const *s2)
{
	char	*str;
	int		i;
	int		s12l;

	if (!s1 || !s2)
		return (NULL);
	s12l = ft_strlen(s1) + ft_strlen(s2);
	i = 0;
	str = malloc(sizeof(char) * s12l + 1);
	if (!str)
		return (NULL);
	while (*s1)
	{
		str[i] = *s1;
		i++;
		s1++;
	}
	while (*s2)
	{
		str[i] = *s2;
		i++;
		s2++;
	}
	str[i] = '\0';
	return (str);
}
